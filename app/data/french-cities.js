// Fonction pour calculer la distance entre deux points GPS (formule de Haversine)
export const calculateDistance = (lat1, lng1, lat2, lng2) => {
  const R = 6371; // Rayon de la Terre en km
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a = 
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) * 
    Math.sin(dLng/2) * Math.sin(dLng/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return R * c; // Distance en km
};

// Fonction pour rechercher des villes par nom
export const searchCities = (query) => {
  if (!query || query.length < 2) return [];
  
  const searchTerm = query.toLowerCase();
  
  return frenchCities
    .filter(city => 
      city.name.toLowerCase().includes(searchTerm) ||
      city.region.toLowerCase().includes(searchTerm)
    )
    .sort((a, b) => {
      // Priorité aux villes qui commencent par la recherche
      const aStartsWith = a.name.toLowerCase().startsWith(searchTerm);
      const bStartsWith = b.name.toLowerCase().startsWith(searchTerm);
      
      if (aStartsWith && !bStartsWith) return -1;
      if (!aStartsWith && bStartsWith) return 1;
      
      // Puis tri par population (plus grandes villes en premier)
      return b.population - a.population;
    })
    .slice(0, 10); // Limiter à 10 suggestions
};

// Fonction pour obtenir les villes par catégorie
export const getCitiesByCategory = (category) => {
  return frenchCities.filter(city => city.category === category);
};

// Fonction pour obtenir les villes par région
export const getCitiesByRegion = (region) => {
  return frenchCities.filter(city => city.region === region);
};

// Fonction pour trouver une ville par nom exact
export const findCityByName = (name) => {
  return frenchCities.find(city => 
    city.name.toLowerCase() === name.toLowerCase()
  );
};

// Fonction pour obtenir les villes dans un rayon donné
export const getCitiesInRadius = (centerCity, radiusKm) => {
  if (!centerCity || !centerCity.lat || !centerCity.lng) return [];
  
  return frenchCities.filter(city => {
    if (city.name === centerCity.name) return true; // Inclure la ville centrale
    
    const distance = calculateDistance(
      centerCity.lat, centerCity.lng,
      city.lat, city.lng
    );
    
    return distance <= radiusKm;
  }).sort((a, b) => {
    // Trier par distance
    const distanceA = calculateDistance(centerCity.lat, centerCity.lng, a.lat, a.lng);
    const distanceB = calculateDistance(centerCity.lat, centerCity.lng, b.lat, b.lng);
    return distanceA - distanceB;
  });
}; 