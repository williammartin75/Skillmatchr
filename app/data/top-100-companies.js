// Fonction pour obtenir tous les secteurs uniques
export const getAllSectors = () => {
  const sectors = [...new Set(top100Companies.map(company => company.sector))];
  return sectors.sort();
};

// Fonction pour filtrer par secteur
export const getCompaniesBySector = (sector) => {
  return top100Companies.filter(company => company.sector === sector);
};

// Fonction pour rechercher par nom
export const searchCompaniesByName = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return top100Companies.filter(company => 
    company.name.toLowerCase().includes(lowercaseQuery) ||
    company.description.toLowerCase().includes(lowercaseQuery)
  );
}; 

