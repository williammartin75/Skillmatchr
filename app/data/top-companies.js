// Fonction pour obtenir les entreprises par secteur
export const getCompaniesBySector = (sector) => {
  return topCompanies.filter(company => company.sector === sector);
};

// Fonction pour obtenir tous les secteurs uniques
export const getAllSectors = () => {
  return [...new Set(topCompanies.map(company => company.sector))];
};

// Fonction pour rechercher des entreprises par nom
export const searchCompaniesByName = (query) => {
  const searchTerm = query.toLowerCase();
  return topCompanies.filter(company => 
    company.name.toLowerCase().includes(searchTerm) ||
    company.description.toLowerCase().includes(searchTerm)
  );
}; 